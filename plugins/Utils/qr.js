import QRCode from 'qrcode';
import { getFakeQuoted } from '../../lib/fakeQuoted.js';

export default {
    name: 'qr',
    alias: ['qrcode', 'qrgen'],
    description: 'Generate a QR code from text or link',
    run: async (context) => {
        const { client, m, text, prefix } = context;
        const fq = getFakeQuoted(m);
        await client.sendMessage(m.chat, { react: { text: 'вҢӣ', key: m.reactKey } });
        if (!text) {
            await client.sendMessage(m.chat, { react: { text: 'вқҢ', key: m.reactKey } }).catch(() => {});
            return m.reply(`в•ӯв”Ғв”Ғв”Ғб•ҷ    б–ҙб—ҙб—ҙ-бҷӯб—°б—Әгғ„    б•—в”Ғв”Ғв”Ғ\nв”ңв”Ғв”Ғв”Ғвү« QR CODE вүӘв”Ғв”Ғв”Ғ\nв”ң \nв”ң Usage: ${prefix}qr <text or link>\nв•°в”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғб•—\n> В©рқ–•рқ–”рқ–ңрқ–Ҡрқ–—рқ–Ҡрқ–ү рқ–Үрқ–һ рқ–Ӣрқ–—рқ–Ҡрқ–үрқ–Һ_рқ–Ҡрқ–ҹрқ–—рқ–Ҷ`);
        }
        try {
            await client.sendMessage(m.chat, { react: { text: 'вҢӣ', key: m.reactKey } });
            const dataUrl = await QRCode.toDataURL(text.slice(0, 2000), { scale: 8, errorCorrectionLevel: 'H' });
            const base64 = dataUrl.replace(/^data:image\/png;base64,/, '');
            const imgBuffer = Buffer.from(base64, 'base64');
            await client.sendMessage(m.chat, { react: { text: 'вң…', key: m.reactKey } });
            await client.sendMessage(m.chat, {
                image: imgBuffer,
                caption: `в•ӯв”Ғв”Ғв”Ғб•ҷ    б–ҙб—ҙб—ҙ-бҷӯб—°б—Әгғ„    б•—в”Ғв”Ғв”Ғ\nв”ңв”Ғв”Ғв”Ғвү« QR CODE вүӘв”Ғв”Ғв”Ғ\nв”ң Scan with any QR reader.\nв•°в”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғб•—\n> В©рқ–•рқ–”рқ–ңрқ–Ҡрқ–—рқ–Ҡрқ–ү рқ–Үрқ–һ рқ–Ӣрқ–—рқ–Ҡрқ–үрқ–Һ_рқ–Ҡрқ–ҹрқ–—рқ–Ҷ`
            }, { quoted: fq });
        } catch {
            await client.sendMessage(m.chat, { react: { text: 'вқҢ', key: m.reactKey } });
            m.reply('Failed to generate QR code.');
        }
    }
};
