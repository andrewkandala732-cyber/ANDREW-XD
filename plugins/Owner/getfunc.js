import { promises as fs } from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { dirname } from 'path';
const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
import { getFakeQuoted } from '../../lib/fakeQuoted.js';

const normalizeNumber = (jid) => {
    if (!jid) return '';
    return jid.split('@')[0].split(':')[0].replace(/\D/g, '') + '@s.whatsapp.net';
};

const DEVELOPER = normalizeNumber('254104959129');
const FEATURES_DIR = path.join(__dirname, '..', '..', 'features');

export default async (context) => {
    const { client, m, text, prefix } = context;
    const fq = getFakeQuoted(m);
    await client.sendMessage(m.chat, { react: { text: 'рҹ”Қ', key: m.reactKey } });

    if (normalizeNumber(m.sender) !== DEVELOPER) {
        await client.sendMessage(m.chat, { react: { text: 'вҢӣ', key: m.reactKey } });
        return await client.sendMessage(m.chat, {
            text: `в•ӯв”Ғв”Ғв”Ғб•ҷ     рқ—”рқ—Ўрқ——рқ—Ҙрқ—ҳрқ—Ә рқ—«рқ——гғ„    б•—в”Ғв”Ғв”Ғ\nв”ңв”Ғв”Ғв”Ғвү« ACCESS DENIED вүӘв”Ғв”Ғв”Ғ\nв”ң \nв”ң This command is restricted to the bot owner.\nв•°в”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғб•—\n> В©рқ–•рқ–”рқ–ңрқ–Ҡрқ–—рқ–Ҡрқ–ү рқ–Үрқ–һ kandala`
        }, { quoted: fq });
    }

    if (!text) {
        let files = [];
        try { const entries = await fs.readdir(FEATURES_DIR); files = entries.filter(f => f.endsWith('.js')); } catch {}
        const fileList = files.map(f => `в”ң вҖў ${f.replace('.js', '')}`).join('\n');
        return await client.sendMessage(m.chat, {
            text: `в•ӯв”Ғв”Ғв”Ғб•ҷ     рқ—”рқ—Ўрқ——рқ—Ҙрқ—ҳрқ—Ә рқ—«рқ——гғ„    б•—в”Ғв”Ғв”Ғ\nв”ңв”Ғв”Ғв”Ғвү« GETFUNC вүӘв”Ғв”Ғв”Ғ\nв”ң \nв”ң Usage: ${prefix}getfunc <name>\nв”ң \nв”ң Available features:\n${fileList || 'в”ң (none found)'}\nв•°в”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғб•—\n> В©рқ–•рқ–”рқ–ңрқ–Ҡрқ–—рқ–Ҡрқ–ү рқ–Үрқ–һ kandala`
        }, { quoted: fq });
    }

    const funcName = text.trim().endsWith('.js') ? text.trim().slice(0, -3) : text.trim();
    const filePath = path.join(FEATURES_DIR, `${funcName}.js`);

    try {
        const data = await fs.readFile(filePath, 'utf8');
        const fileBuffer = Buffer.from(data, 'utf8');

        await client.sendMessage(m.chat, {
            text: `в•ӯв”Ғв”Ғв”Ғб•ҷ     рқ—”рқ—Ўрқ——рқ—Ҙрқ—ҳрқ—Ә рқ—«рқ——гғ„    б•—в”Ғв”Ғв”Ғ\nв”ңв”Ғв”Ғв”Ғвү« FEATURE FILE вүӘв”Ғв”Ғв”Ғ\nв”ң \nв”ң File: ${funcName}.js\nв”ң Size: ${data.length} chars\nв”ң \n\`\`\`javascript\n${data}\n\`\`\`\nв•°в”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғб•—\n> В©рқ–•рқ–”рқ–ңрқ–Ҡрқ–—рқ–Ҡрқ–ү рқ–Үрқ–һ kandala`
        }, { quoted: fq });

        await client.sendMessage(m.chat, {
            document: fileBuffer,
            fileName: `${funcName}.js`,
            mimetype: 'application/javascript',
            caption: `в•ӯв”Ғв”Ғв”Ғб•ҷ     рқ—”рқ—Ўрқ——рқ—Ҙрқ—ҳрқ—Ә рқ—«рқ——гғ„    б•—в”Ғв”Ғв”Ғ\nв”ң рҹ“„ ${funcName}.js\nв”ң Folder: features/\nв”ң Size: ${data.length} chars\nв•°в”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғб•—\n> В©рқ–•рқ–”рқ–ңрқ–Ҡрқ–—рқ–Ҡрқ–ү рқ–Үрқ–һ kandala`
        }, { quoted: fq });

    } catch (err) {
        if (err.code === 'ENOENT') {
            let files = [];
            try { const entries = await fs.readdir(FEATURES_DIR); files = entries.filter(f => f.endsWith('.js')); } catch {}
            const fileList = files.map(f => `в”ң вҖў ${f.replace('.js', '')}`).join('\n');
            return await client.sendMessage(m.chat, {
                text: `в•ӯв”Ғв”Ғв”Ғб•ҷ     рқ—”рқ—Ўрқ——рқ—Ҙрқ—ҳрқ—Ә рқ—«рқ——гғ„    б•—в”Ғв”Ғв”Ғ\nв”ңв”Ғв”Ғв”Ғвү« NOT FOUND вүӘв”Ғв”Ғв”Ғ\nв”ң \nв”ң "${funcName}" not found in features/.\nв”ң \nв”ң Available:\n${fileList || 'в”ң (none found)'}\nв•°в”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғб•—\n> В©рқ–•рқ–”рқ–ңрқ–Ҡрқ–—рқ–Ҡрқ–ү рқ–Үрқ–һ kandala`
            }, { quoted: fq });
        }
        return await client.sendMessage(m.chat, {
            text: `в•ӯв”Ғв”Ғв”Ғб•ҷ     рқ—”рқ—Ўрқ——рқ—Ҙрқ—ҳрқ—Ә рқ—«рқ——гғ„    б•—в”Ғв”Ғв”Ғ\nв”ңв”Ғв”Ғв”Ғвү« ERROR вүӘв”Ғв”Ғв”Ғ\nв”ң \nв”ң Error reading file: ${err.message}\nв•°в”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғб•—\n> В©рқ–•рқ–”рқ–ңрқ–Ҡрқ–—рқ–Ҡрқ–ү рқ–Үрқ–һ kandala`
        }, { quoted: fq });
    }
};
